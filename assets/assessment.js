//–§—É–Ω–∫—Ü–∏—è –∞–Ω–∞–ª–∏–∑–∞
function analyzeAnswer(competence, answer) {
    const matrix = competenceMatrix[competence];
    if (!matrix) return 2; // –°—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é

    let totalScore = 0;
    let keywordCount = 0;
    const text = answer.toLowerCase();
    
    // –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–æ —É—Ä–æ–≤–Ω—è–º –æ—Ç –≤—ã—Å—à–µ–≥–æ –∫ –Ω–∏–∑—à–µ–º—É
    for (let score = 4; score >= 1; score--) {
        const keywords = matrix[score];
        let levelScore = 0;
        let levelCount = 0;
        
        for (const keyword of keywords) {
            // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫ —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏—è, —Ç–∞–∫ –∏ –æ—Ç–¥–µ–ª—å–Ω—ã–µ —Å–ª–æ–≤–∞
            const words = keyword.split(' ');
            
            if (words.length > 1) {
                // –î–ª—è —Å–ª–æ–≤–æ—Å–æ—á–µ—Ç–∞–Ω–∏–π - —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
                if (text.includes(keyword)) {
                    levelScore += score;
                    levelCount++;
                    console.log(`Found phrase: "${keyword}" - ${score} points`);
                }
            } else {
                // –î–ª—è –æ—Ç–¥–µ–ª—å–Ω—ã—Ö —Å–ª–æ–≤ - –ø—Ä–æ–≤–µ—Ä—è–µ–º –≤—Ö–æ–∂–¥–µ–Ω–∏–µ
                if (text.includes(keyword)) {
                    levelScore += score * 0.5; // –û—Ç–¥–µ–ª—å–Ω—ã–µ —Å–ª–æ–≤–∞ –¥–∞—é—Ç –ø–æ–ª–æ–≤–∏–Ω—É –±–∞–ª–ª–æ–≤
                    levelCount++;
                    console.log(`Found word: "${keyword}" - ${score * 0.5} points`);
                }
            }
        }
        
        if (levelCount > 0) {
            totalScore += levelScore;
            keywordCount += levelCount;
        }
    }
    
    // –ë–æ–Ω—É—Å –∑–∞ –≥–ª—É–±–∏–Ω—É –æ—Ç–≤–µ—Ç–∞
    const wordCount = text.split(/\s+/).length;
    const lengthBonus = Math.min(wordCount / 50, 1) * 0.5;
    
    // –ò—Ç–æ–≥–æ–≤—ã–π –±–∞–ª–ª
    let finalScore;
    if (keywordCount > 0) {
        finalScore = (totalScore / keywordCount) + lengthBonus;
    } else {
        finalScore = 1 + lengthBonus; // –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –±–∞–ª–ª + –±–æ–Ω—É—Å –∑–∞ –¥–ª–∏–Ω—É
    }
    
    // –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –∏ –æ–∫—Ä—É–≥–ª—è–µ–º
    finalScore = Math.min(finalScore, 4);
    finalScore = Math.round(finalScore * 2) / 2; // –û–∫—Ä—É–≥–ª–µ–Ω–∏–µ –¥–æ 0.5
    
    console.log(`Analysis result: ${finalScore} (keywords: ${keywordCount}, length bonus: ${lengthBonus.toFixed(2)})`);
    
    return finalScore;
}

